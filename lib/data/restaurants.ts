export function getRestaurantById(id: string): Restaurant | undefined {
  return restaurants.find((r) => r.id === id)
}

export function getFeaturedRestaurants(): Restaurant[] {
  return restaurants.filter((r) => r.featured).slice(0, 6)
}

export function getTopRatedRestaurants(): Restaurant[] {
  return [...restaurants].sort((a, b) => b.rating - a.rating).slice(0, 4)
}

export function getAllCuisines(): string[] {
  const cuisines = new Set<string>()
  restaurants.forEach((r) => r.cuisine.forEach((c) => cuisines.add(c)))
  return Array.from(cuisines).sort()
}

export function getAllLocations(): string[] {
  const locations = new Set<string>()
  restaurants.forEach((r) => locations.add(r.location))
  return Array.from(locations).sort()
}
